# Stat types that display as negative (reduction)
_NEGATIVE_STATS = {"SkillCooldownMulti"}


def fmt_stat(stat_type_name: str, perfection: float) -> str:
    ranges  = STAT_RANGES.get(stat_type_name, {})
    lower   = ranges.get("lower", 0.0)
    upper   = ranges.get("upper", 1.0)
    value   = lower + (upper - lower) * perfection
    pct     = value * 100
    sign    = "-" if stat_type_name in _NEGATIVE_STATS else "+"
    display = STAT_DISPLAY_NAMES.get(stat_type_name, stat_type_name)
    return f"{sign}{pct:.2f}% {display}"


def mount_display_name(detail: str) -> str:
    for data in MOUNT_MAPPING.values():
        if data["Name"].replace(" ", "") == detail:
            return data["Name"]
    return detail


def main():
    player = parse_dump(DUMP)

    # ── Egg hatch prediction (only equipped / in-slot eggs) ──────────────────
    hatch_eggs = [e for e in player.pets.eggs if e.is_equipped]
    hatch_eggs.sort(key=lambda e: e.equip_slot)

    print("=" * 65)
    print("  EGG HATCH PREDICTION  (currently in hatch slots)")
    print("=" * 65)
    for egg in hatch_eggs:
        pred  = predict_hatch(egg)
        stats = pred.secondary_stats.stats
        stat_parts = [fmt_stat(s.stat_type.name, s.perfection) for s in stats]
        stats_str  = "  |  ".join(stat_parts) if stat_parts else "-"
        print(f"  Slot {egg.equip_slot + 1}  [{pred.rarity.name}] {pred.pet_name}")
        print(f"         -> {stats_str}")
        print(f"         (seed: {egg.seed:#018x})")
        print()

    # ── Mount summon x5 (1 at a time) ────────────────────────────────────────
    gl = GameLogic(player)
    sm = player.mounts.summon_model
    print("=" * 65)
    print(f"  MOUNT SUMMON x5")
    print(f"  (seed={sm.get_seed():#018x}  level={sm.level}  count={sm.count})")
    print("=" * 65)
    pull_num = 0
    for _ in range(5):
        result = gl.summon_mounts(1)
        if not result.success:
            print(f"  FAILED: {result.error}")
            break
        for p in result.pulls:
            pull_num += 1
            bonus = "  (BONUS)" if p.is_bonus else ""
            new   = " [NEW]"   if p.is_new   else ""
            name  = mount_display_name(p.detail)
            if p.secondary_stats and p.secondary_stats.stats:
                stat_parts = [fmt_stat(s.stat_type.name, s.perfection)
                              for s in p.secondary_stats.stats]
                stats_str  = "  |  ".join(stat_parts)
                print(f"  #{pull_num}  [{p.rarity.name}] {name}{new}{bonus}")
                print(f"       -> {stats_str}")
            else:
                print(f"  #{pull_num}  [{p.rarity.name}] {name}{new}{bonus}")
    print(f"\n  after: seed={sm.get_seed():#018x}  level={sm.level}  count={sm.count}")


if __name__ == "__main__":
    main()
