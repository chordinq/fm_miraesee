def main():
	import argparse
	parser = argparse.ArgumentParser()
	parser.add_argument("-n", "--hammers", type=int, default=5)
	args = parser.parse_args()

	player = parse_dump(DUMP)
	print(f"Forge seed : {player.forge.forge_seed:#018x}")
	print(f"Forge level: {player.forge.forge_level} (UI shows +1)")
	print(f"Hammers    : {player.get_currency(CurrencyType.Hammers):,}")
	print(f"FreeForge tech level: {player.get_tech_level(TechTreeNodeType.FreeForgeChance)}")
	print()

	sim = ForgeSimulator(player)
	result = sim.forge(args.hammers)
	if not result.success:
		print("FAILED:", result.error)
		return

	print(f"Requested {result.requested_hammers} | actual {result.actual_hammers} | free bonus {result.free_forge_count}")
	print(f"Next seed: {player.forge.forge_seed:#018x}")
	print()
	for i, pull in enumerate(result.pulls, 1):
		print(sim.format_pull(pull, i))


if __name__ == "__main__":
	main()
